public class 문제_1_1 {
  public static void main(String[] args) {
    int i = 0; // i -> index(목차, 순서)
    while(i<5){
      System.out.println("*");
      ++i;

    //---------------------------------------//
    for(int j=0; j<5; ++j){
      System.out.println("*");
    }


    }
  }
}

