public class 문제_1_12 {
  public static void main(String[] args) {
    int a = 1;
    while(a<10){
      System.out.println("2 * " + a + " = " + 2*a);
      ++a;
    }
    /// ////////////////////////////////////////
    for(int i=1; i<10; i++){
      System.out.println("2 * " + i + " = " + 2*i);
    }



  }
}
