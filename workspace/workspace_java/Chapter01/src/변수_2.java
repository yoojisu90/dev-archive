public class 변수_2 {
  public static void main(String[] args) {
    //정수 하나를 저장할 수 있는 변수 money를 선언
    int money;

    //변수 money에 숫자 100을 저장
    money = 100;

    //변수 money의 값을 출력
    System.out.println("money = " + money);

    money = 200;
    System.out.println("money =" + money);

    money = money - 50;
    System.out.println("money =" + money);



  }
}
