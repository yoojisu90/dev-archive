public class 변수_1 {
  public static void main(String[] args) {
    //변수의 선언 문법
    //자료형 변수명;
    int num1; //정수 하나를 저장할 수 있는 변수 num1을 선언!

    //프로그래밍언어에서 '=' 기호는 '같다'라는 의미가 아니다!!
    //우측값을 왼쪽에 저장한다(할당한다)
    //선언된 변수에 최초로 값을 저장하는 행위를 '변수의 초기화'라고 한다.
    num1 = 10; //10을 num1에 저장한다.
    num1 = 20;


  }
}
