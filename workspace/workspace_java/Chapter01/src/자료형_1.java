public class 자료형_1 {
  public static void main(String[] args) {

    //참 또는 거짓 데이터를 저장하는 자료형
    boolean bool = true; //참
    boolean bool1 = false; //거짓
    System.out.println(bool);

    double a = 5.5;
    System.out.println(a);

    //자바는 문자와 문자열을 구분한다.
    //문자 : 홀따옴표로 감싼 한 글자.
    //문자열 : 쌍따옴표로 감싼 0자 이상의 문자.
    //문자열의 예시 : ""," ", "java", "A"
    char c = 'j';
    System.out.println(c);

  }
}
