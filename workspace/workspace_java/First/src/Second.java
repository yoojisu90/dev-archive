// 모든 자바 코드는 클래스 안에 위치한 main 메서드(Method) 내부에 작성한다.
// 명령어 뒤에는 반드시 세미콜론을 붙인다.
public class Second {
  public static void main(String[] args){
    // 출력문 - 소괄호 안의 내용을 출력하고, 한 줄 개행한다.
    System.out.println("hello~");
    System.out.println("안녕하세요");

    // 출력문 - 소괄호 안의 내용을 출력한다.
    System.out.print("hi~");
    System.out.print("hi~");
  }
}

