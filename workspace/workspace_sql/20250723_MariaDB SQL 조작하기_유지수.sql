#5번
SELECT EMPNO,ENAME,JOB,HIREDATE
FROM emp
WHERE ENAME LIKE '%병%' OR ENAME LIKE '%김%'
ORDER BY JOB ASC, EMPNO DESC;

#6번
SELECT
	EMPNO,
	ENAME,
	DEPTNO,
	CASE DEPTNO
		WHEN 10 THEN '인사부'
		WHEN 20 THEN '영업부'
		WHEN 30 THEN '개발부'
		ELSE '생산부' END AS 부서명
FROM emp;

#7번
SELECT EMPNO,ENAME,TO_CHAR(HIREDATE, 'YYYY.MM.DD HH24.MI.SS')
FROM emp
WHERE MOD(MONTH(HIREDATE),2)=0
AND JOB IN('사원','과장','차장')
ORDER BY HIREDATE ASC;

#8번
SELECT EMPNO,ENAME,SAL,COMM,
		(SAL * 12) + IFNULL(COMM,0) AS TOTAL_SALARY
FROM emp
ORDER BY SAL DESC;		

#9번
SELECT EMPNO,DEPTNO,SUBSTR(ENAME,2) AS 이름
FROM emp
WHERE YEAR(HIREDATE) BETWEEN 2003 AND 2006;

