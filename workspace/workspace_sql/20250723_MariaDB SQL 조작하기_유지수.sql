CREATE TABLE BOOK_INFO(
	BOOK_NUM INT PRIMARY KEY,
	BOOK_NAME VARCHAR(10) NOT NULL,
	WRITER VARCHAR(10) NOT NULL,
	PRICE INT,
	PUBLISHER VARCHAR(10) DEFAULT '미지정',
	PUB_DATE DATE
);

